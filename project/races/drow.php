    <h2>Racial Traits</h2>
  </div>
  <ul>
    <li><b>Ability Score Modifiers:</b> Drow are nimble and manipulative. They gain +2 Dexterity, +2 Charisma, and –2 Constitution.</li>
    <li><b>Size:</b>Drow are Medium creatures and thus receive no bonuses or penalties due to their size.</li>
    <li><b>Type:</b>Drow are humanoids with the elf subtype.</li>
    <li><b>Base Speed:</b>Drow have a base speed of 30 feet.</li>
    <li><b>Languages:</b> Drow begin play speaking Elven and Undercommon. Drow with high Intelligence scores can choose from the following languages: Abyssal, Aklo, Aquan, Common, Draconic, Drow Sign Language, Gnome, or Goblin. See the Linguistics skill page for more information about these languages.</li>
    <li><b>Immunities:</b>  Drow are immune to magic sleep effects and gain a +2 racial bonus on saving throws against enchantment spells and effects.</li>
    <li><b>Spell Resistance:</b> Drow possess spell resistance (SR) equal to 6 plus their total number of class levels.</li>
    <li><b>Keen Senses:</b>  Drow gain a +2 racial bonus on Perception checks.</li>
    <li><b>Weapon Familiarity:</b>Drow are proficient with the hand crossbow, rapier, and shortsword.</li>
    <li><b>Spell-Like Abilities (Su)</b>  Drow can cast dancing lights, darkness, and faerie fire, once each per day, using their total character level as caster level.</li>
    <li><b>Poison Use:</b>Drow are skilled in the use of poisons and never risk accidentally poisoning themselves.</li>
    <li><b>Superior Darkvision: </b>Drow have superior darkvision, allowing them to see perfectly in the dark up to 120 feet.</li>
    <li><b>Light Blindness:</b>As deep underground dwellers naturally, drow suffer from light blindness. Abrupt exposure to any bright light blinds drow for 1 round. On subsequent rounds, they are dazzled as long as they remain in the affected area.</li>
